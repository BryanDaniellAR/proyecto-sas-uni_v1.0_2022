BEGIN TRANSACTION;
CREATE TABLE IF NOT EXISTS "ALUMNO" (
	"cod_alumno"	CHAR(9),
	"nombre"	VARCHAR(40),
	"edad"	INTEGER,
	"ciclo"	INTEGER,
	"especialidad"	CHAR(2),
	PRIMARY KEY("cod_alumno")
);
CREATE TABLE IF NOT EXISTS "AULA" (
	"cod_aula"	CHAR(6),
	"descripcion_a"	VARCHAR(20),
	PRIMARY KEY("cod_aula")
);
CREATE TABLE IF NOT EXISTS "CURSO" (
	"cod_curso"	CHAR(5),
	"nom_curso"	VARCHAR(200),
	"creditos"	INTEGER,
	"especialidades"	VARCHAR(4),
	"observacion"	CHAR(1),
	PRIMARY KEY("cod_curso")
);
CREATE TABLE IF NOT EXISTS "CURSO_SECCION" (
	"cod_clase"	CHAR(6),
	"cod_curso"	CHAR(5),
	FOREIGN KEY("cod_curso") REFERENCES "CURSO"("cod_curso"),
	PRIMARY KEY("cod_clase")
);
CREATE TABLE IF NOT EXISTS "DOCENTE" (
	"cod_docente"	CHAR(9),
	"nombre"	VARCHAR(40),
	"clave"	VARCHAR(40),
	PRIMARY KEY("cod_docente")
);
CREATE TABLE IF NOT EXISTS "HORARIO_CLASE" (
	"cod_horario_clase"	INTEGER IDENTITY(1, 1),
	"fecha"	VARCHAR(10),
	"hora_inicio"	INTEGER,
	"hora_final"	INTEGER,
	"tipo"	VARCHAR(40),
	"cod_docente"	CHAR(9),
	"cod_clase"	CHAR(6),
	"cod_aula"	CHAR(6),
	FOREIGN KEY("cod_docente") REFERENCES "DOCENTE"("cod_docente"),
	FOREIGN KEY("cod_aula") REFERENCES "AULA"("cod_aula"),
	FOREIGN KEY("cod_clase") REFERENCES "CURSO_SECCION"("cod_clase"),
	PRIMARY KEY("cod_horario_clase")
);
CREATE TABLE IF NOT EXISTS "ASISTENCIA" (
	"cod_asistencia"	INTEGER,
	"fecha_m"	DATE,
	"hora_m"	VARCHAR(8),
	"cod_horario_clase"	INTEGER,
	"cod_alumno"	CHAR(9),
	FOREIGN KEY("cod_alumno") REFERENCES "ALUMNO"("cod_alumno"),
	FOREIGN KEY("cod_horario_clase") REFERENCES "HORARIO_CLASE"("cod_horario_clase"),
	PRIMARY KEY("cod_asistencia" AUTOINCREMENT)
);
CREATE TABLE IF NOT EXISTS "MATRICULADO" (
	"cod_matriculado"	INTEGER IDENTITY(1, 1),
	"cod_curso_seccion"	CHAR(6),
	"cod_alumno"	CHAR(9),
	"cod_periodo"	CHAR(6),
	FOREIGN KEY("cod_curso_seccion") REFERENCES "CURSO_SECCION"("cod_clase"),
	FOREIGN KEY("cod_alumno") REFERENCES "ALUMNO"("cod_alumno"),
	FOREIGN KEY("cod_periodo") REFERENCES "PERIODO"("cod_periodo"),
	PRIMARY KEY("cod_matriculado")
);
CREATE TABLE IF NOT EXISTS "PERIODO" (
	"cod_periodo"	INTEGER NOT NULL,
	"descripcion"	TEXT NOT NULL,
	"fecha_inicio"	DATE NOT NULL,
	"fecha_fin"	DATE NOT NULL,
	PRIMARY KEY("cod_periodo" AUTOINCREMENT)
);
